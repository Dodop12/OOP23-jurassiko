package it.unibo.jurassiko.controller.api;

/**
 * Interface for the Starting Menu.
 */
public interface StartController {

    /**
     * Start the Main Menu view.
     */
    void startView();

    /**
     * Start the game.
     */
    void startGame();

    /**
     * Close the Main Menu View.
     */
    void quitView();
}
